    public int nameCbbToId(int checkCbb, String name) {
        if (checkCbb == 0) {//Department
            DepartmentDAO dDAO = new DepartmentDAO();
            Department d = new Department();
            d = dDAO.selectById(name);
            return d.getIdDepartment();
        } else if (checkCbb == 1) {//Duty
            DutyDAO dDAO = new DutyDAO();
            Duty d = new Duty();
            d = dDAO.selectById(name);
            return d.getIdDuty();
        }
        if (checkCbb == 2) {//Degree
            DegreeDAO dDAO = new DegreeDAO();
            Degree d = new Degree();
            d = dDAO.selectById(name);
            return d.getIdDegree();
        }
        return 0;
    }

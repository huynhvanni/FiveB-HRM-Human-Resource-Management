    public void first() {
        this.row = 0;
        tbl.setRowSelectionInterval(row, row);
        this.edit();

    }

    public void prev() {
        if (this.row > 0) {
            this.row--;
            tbl.setRowSelectionInterval(row, row);
            this.edit();
        }
    }

    public void next() {
        if (this.row < tbl.getRowCount() - 1) {
            this.row++;
            tbl.setRowSelectionInterval(row, row);
            this.edit();
        }
    }

    public void last() {
        this.row = tbl.getRowCount() - 1;
        tbl.setRowSelectionInterval(row, row);
        this.edit();
    }


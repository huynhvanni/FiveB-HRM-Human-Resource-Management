    // fill data from database to table
    public void fillTable() {
        DefaultTableModel model = (DefaultTableModel) tbl.getModel();
        model.setRowCount(0);
        listStaff.clear();
        try {
            List<com.Nhom5.Entity.Employee> list = dao.selectAll(); // Đọc dữ liệu từ CSDL
            for (com.Nhom5.Entity.Employee e : list) {
                Object[] data = {
                    e.getIdEmployee(),
                    e.getNameEmployee(),
                    e.isGender() ? "Nam" : "Nữ",
                    e.getBirthday(),
                    e.getPhoneNumber(),
                    e.getCityzenIdentyCard(),
                    e.getAddress(),
                    e.getImg(),
                    e.getNameDepartment(),
                    e.getNameDuty(),
                    e.getNameDegree(),
                    e.getSalaryBasic(),};
                listStaff.add(e);
                model.addRow(data);
            }
        } catch (Exception e) {
            MsgBox.alert(this, "Lỗi truy vấn dữ liệu!");
        }
    }


    public void delete() {
        String id = txt_Id.getText();

        if (MsgBox.confirm(this, "Bạn thực sự muốn xóa nhân sự này?")) {
            try {
                dao.delete(id);
                this.fillTable();
                this.clearForm();
                MsgBox.alert(this, "Xóa thành công");
            } catch (Exception e) {
                MsgBox.alert(this, "Xóa thất bại");
            }
        }
    }

    public void update() {
        if (checkInputData() == true) {
            com.Nhom5.Entity.Employee emp = getForm();
            try {
                dao.update(emp);
                this.fillTable();
                clearForm();
                MsgBox.alert(this, "Cập nhật thành công");
            } catch (Exception e) {
                MsgBox.alert(this, "Cập nhật thất bại");

            }
        }

    }

    public void insert() {
        dao.insert(getForm());
        fillTable();
        clearForm();
        MsgBox.alert(this, "Thêm mới thành công");
    }

